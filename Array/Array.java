package Array;
public class Array {
    public static void main(String[] args) {
        // Initializing an Array
        int[] arr = new int[3];

        // Assigning the value to the index of an Array
        arr[0] = 1;
        arr[1] = 2;
        arr[2] = 3;

        // Accessing an Element
        System.out.println(arr[1]); // Result 2

    }
}
